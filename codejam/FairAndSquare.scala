    //http://mathforum.org/library/drmath/view/51510.html

    val input = processedInput.toVector
    val endpoints = input(0).split(" ")
    val start = BigInt(endpoints(0))
    val end = BigInt(endpoints(1))

    val result = table.foldLeft(0) { (sum:Int, entry:BigInt) =>
      if (entry >= start && entry <= end)
        sum + 1
      else
        sum
    }

    result.toString
  }
